export function getActivityBySlug(slug: string): Activity | undefined {
  return activities.find((a) => a.slug === slug);
}

export function getActivitiesByCategory(category: Category): Activity[] {
  return activities.filter((a) => a.category === category);
}

export function getActivitiesBySeason(season: string): Activity[] {
  return activities.filter((a) => a.seasons.includes(season as Season));
}

export function getFeaturedActivities(): Activity[] {
  return activities.filter((a) => a.featured);
}

export function getActivitiesWithDeals(): Activity[] {
  return activities.filter((a) => a.deal);
}

export function searchActivities(query: string): Activity[] {
  const lower = query.toLowerCase();
  return activities.filter(
    (a) =>
      a.name.toLowerCase().includes(lower) ||
      a.description.toLowerCase().includes(lower) ||
      a.tags.some((t) => t.includes(lower)) ||
      a.location.city.toLowerCase().includes(lower) ||
      a.location.region.toLowerCase().includes(lower) ||
      a.category.includes(lower)
  );
}
